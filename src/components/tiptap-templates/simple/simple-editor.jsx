export function SimpleEditor() {
  const { currentNote } = useNote();
  // console.log("showing the currentNote", currentNote);
  React.useEffect(() => {
    console.log("🔥 currentNote changed:", currentNote);
  }, [currentNote]);

  console.log("NoteContext identity:", useNote());

  const isMobile = useIsMobile();
  const { height } = useWindowSize();
  const [mobileView, setMobileView] = React.useState("main");
  const toolbarRef = React.useRef(null);

  const editor = useEditor({
    immediatelyRender: false,
    shouldRerenderOnTransaction: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": "Main content area, start typing to enter text.",
        class: "simple-editor",
      },
    },
    extensions: [
      StarterKit.configure({
        horizontalRule: false,
        link: {
          openOnClick: false,
          enableClickSelection: true,
        },
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,
      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: handleImageUpload,
        onError: (error) => console.error("Upload failed:", error),
      }),
    ],
    content: "",
  });

  // 🟩 Load selected note content dynamically
  React.useEffect(() => {
    async function loadNoteContent() {
      if (!currentNote || !editor) return;

      const noteId = currentNote.url; // your sidebar sets note.url = id

      try {
        const res = await fetch(`/api/notes?id=${noteId}`);
        if (!res.ok) throw new Error(`HTTP ${res.status}`);

        const data = await res.json();

        // Load the HTML (or JSON) content into Tiptap
        editor.commands.setContent(data.content || "", false);

        console.log("✅ Loaded note content:", data.content);
      } catch (error) {
        console.error("❌ Error loading note content:", error);
      }
    }

    loadNoteContent();
  }, [currentNote, editor]);

  const rect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  });

  React.useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main");
    }
  }, [isMobile, mobileView]);

  const saveCollection = () => {
    if (!editor) return;
    const html = editor.getHTML();
    const json = editor.getJSON();
    console.log("Saving the note...", html);

    fetch("/api/notesCollection", {
      method: "POST",
      headers: { "Content-type": "application/json" },
      body: JSON.stringify({ content: html }),
    })
      .then((res) => {
        if (!res.ok) throw new Error(`status ${res.status}`);
        return res.json();
      })
      .then((data) => console.log("Saved OK", data))
      .catch((err) => console.error("Save failed", err));
  };

  return (
    <div className="simple-editor-wrapper">
      <EditorContext.Provider value={{ editor }}>
        <Toolbar
          ref={toolbarRef}
          style={{
            ...(isMobile
              ? {
                  bottom: `calc(100% - ${height - rect.y}px)`,
                }
              : {}),
          }}
        >
          {mobileView === "main" ? (
            <MainToolbarContent
              onHighlighterClick={() => setMobileView("highlighter")}
              onLinkClick={() => setMobileView("link")}
              isMobile={isMobile}
              onSave={saveCollection} // <-- Save
            />
          ) : (
            <MobileToolbarContent
              type={mobileView === "highlighter" ? "highlighter" : "link"}
              onBack={() => setMobileView("main")}
            />
          )}
        </Toolbar>

        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content"
        />
      </EditorContext.Provider>
    </div>
  );
}
